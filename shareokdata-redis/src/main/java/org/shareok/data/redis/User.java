package org.shareok.data.redis;

import java.util.Date;

/**
 *
 * @author Tao Zhao
 */
public class User {
    private String userName;  //optional, by default to be user's email
    private String email;  // required
    private String password;  // required
    private String userId;  //index stored in DB, generated by system
    private Date startTime;
    private boolean isActive; // by default to be active

    public String getUserName() {
        return userName;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }

    public String getUserId() {
        return userId;
    }

    public Date getStartTime() {
        return startTime;
    }

    public boolean isIsActive() {
        return isActive;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    public void setIsActive(boolean isActive) {
        this.isActive = isActive;
    }
    
    
}
